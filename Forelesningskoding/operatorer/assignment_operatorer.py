x = 5
print(f"x = {x}")

x += 3      # x = x + 3
print(f"x = {x}")

x -= 2      # x = x - 2
print(f"x = {x}")

x *= 3      # x = x * 2
print(f"x = {x}")

x /= 3      # x = x / 3
print(f"x = {x:.0f}")

x %= 5      # x = x % 5
print(f"x = {x}")

x **= 2      # x = x ** 2
print(f"x = {x}")

x //= 3      # x = x // 3
print(f"x = {x}")